function titleCase(str: string) {
  var splitStr = str.toLowerCase().split(" ");
  for (var i = 0; i < splitStr.length; i++) {
    // You do not need to check if i is larger than splitStr length, as your for does that for you
    // Assign it back to the array
    splitStr[i] = splitStr[i].charAt(0).toUpperCase() + splitStr[i].substring(1);
  }
  // Directly return the joined string
  return splitStr.join(" ");
}

export const clubs = (ignore: Ligue[]) => {
  const liguesToShow = soccer_leagues_name_search_for_web_scrapping.filter(
    (val) => !ignore.includes(val.league)
  );
  let allClubsLinks: string[] = [];
  liguesToShow.forEach((ligue) => {
    allClubsLinks = allClubsLinks.concat(ligue.linksToClubs);
  });

  return allClubsLinks.map((club) =>
    titleCase(club.slice(29, club.indexOf("/", 29)).replaceAll("-", " "))
  );
};

